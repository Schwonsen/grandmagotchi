package com.uniks.grandmagotchi.data;

public class LivingRoomAttributes
{
	
	private boolean isWatchingTv;
	private boolean isListeningToMusic;
	private boolean isBored;
	
	public boolean isWatchingTv()
	{
		return isWatchingTv;
	}
	public void setWatchingTv(boolean isWatchingTv)
	{
		this.isWatchingTv = isWatchingTv;
	}
	public boolean isListeningToMusic()
	{
		return isListeningToMusic;
	}
	public void setListeningToMusic(boolean isListeningToMusic)
	{
		this.isListeningToMusic = isListeningToMusic;
	}
	public boolean isBored()
	{
		return isBored;
	}
	public void setBored(boolean isBored)
	{
		this.isBored = isBored;
	}

	

}
